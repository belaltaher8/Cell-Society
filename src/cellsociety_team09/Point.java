package cellsociety_team09;

public class Point {
	private int myX;
	private int myY;
	
	public Point(int x, int y) {
		myX = x;
		myY = y;
	}
	
	public int getX() {
		return myX;
	}
	
	public int getY() {
		return myY;
	}
	
	public void setX(int x) {
		myX = x;
	}
	
	public void setY(int y) {
		myY = y;
	}
}
